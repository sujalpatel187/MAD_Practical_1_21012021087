fun main(){
    var a =10;
    println("Integer value: $a");
    var b=a.toDouble();
    println("Double value(from integer): $b");
    var c="10";
    println("String value : $c");
    var d=c.toInt();
    println("Integer value (from String) : $d")
    var e="11.12";
    var f=e.toDouble();
    println("double value(from String) : $f")
}